export class LineIterator {
  lines: LogLine[];
  index: number;
  length: number;

  constructor(lines: LogLine[]) {
    this.lines = lines;
    this.index = 0;
    this.length = lines.length;
  }

  peek(): LogLine | null {
    return this.index < this.length ? this.lines[this.index] || null : null;
  }

  fetch(): LogLine | null {
    return this.index < this.length ? this.lines[this.index++] || null : null;
  }
}

