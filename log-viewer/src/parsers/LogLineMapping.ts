import type { LogEventType, LogLineConstructor } from './types.js';

export function getLogEventClass(
  eventName: LogEventType,
): LogLineConstructor<ApexLogParser, LogEvent> | null | undefined {
  if (!eventName) {
    return null;
  }

  // Fast path for the most commonly occuring types
  switch (eventName) {
    case 'METHOD_ENTRY':
      return MethodEntryLine;

    case 'METHOD_EXIT':
      return MethodExitLine;

    case 'CONSTRUCTOR_ENTRY':
      return ConstructorEntryLine;

    case 'CONSTRUCTOR_EXIT':
      return ConstructorExitLine;

    default:
      break;
  }

  // Handle all other types
  const logType = lineTypeMap.get(eventName);
  if (logType) {
    return logType;
  } else if (basicLogEvents.has(eventName)) {
    return BasicLogLine;
  } else if (basicExitLogEvents.has(eventName)) {
    return BasicExitLine;
  }

  return null;
}

